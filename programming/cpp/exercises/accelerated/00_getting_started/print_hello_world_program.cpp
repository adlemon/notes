// print_hello_world_program.cpp
// Print to the console a C++ program that prints "Hello, World!" to the
// console.

#include <iostream>

int main()
{
  std::cout
    << "// hello_world.cpp\n"
    << "// Print \"Hello, World!\" to the console.\n\n"
    << "#include <iostream>\n\n"
    << "int main()\n"
    << "{\n"
    << "  std::cout << \"Hello, World!\" << std::endl;\n"
    << "  return 0;\n"
    << "}" << std::endl;
  return 0;
}
