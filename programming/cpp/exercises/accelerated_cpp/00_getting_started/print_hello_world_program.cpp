// print_hello_world_program.cpp
// Print to the console a C++ program that prints "Hello, World!" to the
// console.

#include <iostream>

int main()
{
  std::cout << "// hello_world.cpp" << std::endl
    << "// Print \"Hello, World!\" to the console." << std::endl
    << std::endl
    << "#include <iostream>" << std::endl
    << std::endl
    << "int main()" << std::endl
    << "{" << std::endl
    << "  std::cout << \"Hello, World!\" << std::endl;" << std::endl
    << "  return 0;" << std::endl
    << "}" << std::endl;
  return 0;
}
